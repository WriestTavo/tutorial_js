console.log("Igual a:");
console.log(5 == 5) //Aqui muestra true debido que 5 es igual 5.
console.log(5 == 4) // Si colocamos colocamos 4 mostrara false ya que 5 no es igual 4.

console.log(" Diferente que:");
console.log(5 != 5) //Aqui muestra false 5 es diferente 5.
console.log(5 != 4) // Si colocamos colocamos 4 mostrara true ya que 5 si es diferente de  4.

